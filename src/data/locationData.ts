// Get states for a country
export const getStatesForCountry = (countryCode: string) => {
  const country = countriesWithStates[countryCode];
  if (!country) return [];
  return Object.keys(country.states);
};

// Get cities for a state
export const getCitiesForState = (countryCode: string, state: string) => {
  const country = countriesWithStates[countryCode];
  if (!country || !country.states[state]) return [];
  return country.states[state];
};

// Validate zip code
export const validateZipCode = (zipCode: string, countryCode: string): boolean => {
  const pattern = zipCodePatterns[countryCode];
  if (!pattern) return true; // If no pattern, accept any
  return pattern.pattern.test(zipCode);
};

// Get zip code placeholder
export const getZipCodePlaceholder = (countryCode: string): string => {
  const pattern = zipCodePatterns[countryCode];
  if (!pattern) return "Enter postal code";
  return `e.g., ${pattern.example}`;
};
